/** 构建完整的食物数据库 */
function buildFoodDatabase() {
  const categories = [
    { key: 'staple', label: '主食', icon: '🍚', foods: stapleFoods },
    { key: 'meat', label: '肉类', icon: '🥩', foods: meatFoods },
    { key: 'seafood', label: '水产', icon: '🐟', foods: seafoodFoods },
    { key: 'vegetable', label: '蔬菜', icon: '🥬', foods: vegetableFoods },
    { key: 'fruit', label: '水果', icon: '🍎', foods: fruitFoods },
    { key: 'snack', label: '零食', icon: '🍿', foods: snackFoods },
    { key: 'drink', label: '饮品', icon: '🥤', foods: drinkFoods },
    { key: 'bean', label: '豆类蛋奶', icon: '🫘', foods: beanFoods },
    { key: 'seasoning', label: '调味品', icon: '🧂', foods: seasoningFoods },
    { key: 'mcdonalds', label: '麦当劳', icon: '🍔', foods: mcdonaldsFoods },
    { key: 'kfc', label: '肯德基', icon: '🍗', foods: kfcFoods },
  ];

  let id = 0;
  const allFoods = [];
  const byId = {};

  for (const cat of categories) {
    for (const food of cat.foods) {
      id++;
      const fullFood = {
        id,
        name: food.name,
        calories: food.calories,
        protein: food.protein,
        carbs: food.carbs,
        fat: food.fat,
        category: cat.key,
        categoryLabel: cat.label,
        categoryIcon: cat.icon,
      };
      allFoods.push(fullFood);
      byId[id] = fullFood;
    }
  }

  return { allFoods, byId, categories };
}

const foodDB = buildFoodDatabase();

export const { allFoods, byId: foodById, categories: foodCategories } = foodDB;
export const defaultCategory = 'staple';

/** 搜索食物 */
export function searchFoods(query) {
  if (!query || !query.trim()) return [];
  const q = query.trim().toLowerCase();
  return allFoods.filter(f =>
    f.name.toLowerCase().includes(q) ||
    f.categoryLabel.includes(q)
  ).slice(0, 20);
}

/** 获取某分类的所有食物 */
export function getFoodsByCategory(category) {
  return allFoods.filter(f => f.category === category);
}

/** 获取食物总数 */
export function getFoodCount() {
  return allFoods.length;
}
